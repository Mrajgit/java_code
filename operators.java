public class operators {
    public static void main(String[] args) {
        int a=10;
        int b= 10+a;
        int c= 10-a;
        int d= 10*a;
        float e= 10/a;
        int f= 12%a;
        int g=12;
        g +=3;
        System.out.println("addition "+b);
        System.out.println("subtraction "+c);
        System.out.println("multiplication "+d);
        System.out.println("division "+e);
        System.out.println("module op (reminder)"+f);
        System.out.println("assignment operator "+g);
        System.out.println("comparison operator "+(4<5));
        System.out.println("logical operator ");
        System.out.println("and && " +(5>4 && 6<5));
        System.out.println("or || "+(5>4 || 6<5));
    }
}
